package modulo1;

public class a9while {
    public static void main(String[] args) {
        int repeticoes =1;
        while(repeticoes <=5){
            System.out.printf("\nRepeticao %d", repeticoes);
            repeticoes = repeticoes +1;
        }
        repeticoes =1;
        while(repeticoes <=5){
            System.out.printf("\nRepeticao %d", repeticoes);
            repeticoes += 1;
        }
    }
}
