package modulo1.Revisao;

public class a17switchBreak {
    public static void main(String[] args) {
        int opcao = 2;

        switch(opcao){
            case 1:
                System.out.println("Igual a 1");;
                break;

            case 2:
                System.out.println("Igual a 2");;
                break;

            case 3:
                System.out.println("Igual a 3");;
                break;
            default:
                System.out.println("Opção não prevista");
        }
    }
}
